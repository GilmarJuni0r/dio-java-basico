public class Calculadora {

    public static String adicao(int x, int y){
        return x + " + " + y + " = " + (x+y);
    }
    public static String subtracao(int x, int y){
        return x + " - " + y + " = " + (x-y);
    }
    public static String multiplicacao(int x, int y){
        return x + " * " + y + " = " + (x*y);
    }
    public static String divisao(int x, int y){
        return x + " / " + y + " = " + (x/y);
    }
    
}